package res;

public class Res {
	private int num;
	private String name;
	private String date;
	private String content;
	private String add;
	public Res(int num, String name, String date, String content, String add) {
		this.num = num;
		this.name = name;
		this.date = date;
		this.content = content;
		this.add = add;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	
	
	
}
